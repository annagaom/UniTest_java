package data;

public class Laskin
{
    public int summa(int eka, int toka)
    {
        return eka + toka;
    }

    public int erotus(int eka, int toka)
    {
        return eka - toka;
    }

    public int tulo(int eka, int toka)
    {
        return eka * toka;
    }

    public int osamaara(int eka, int toka)
    {
        return eka / toka;
    }
}
